package com.tns.basic;

public class NestedIf {
	
	public static void main(String[] args) {
		
		int x=30,y=10;
		if(x==40)
		{
			if(y==20)
			{
				System.out.println("X=30 and Y=10");
				
			}
		
		else
		{
			System.out.println("Value of x is not equal to 30 and value of y is not equal to 10");
			
		}
	}
		else
		{
			System.out.println("Value of x is not equal to 30");
		}
	
	}
}
	

